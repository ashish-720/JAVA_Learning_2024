package topic5_ConstructorsInJava;

public class BestWishesFromAshish {
    public static void main(String[] args) {
        String bestie = "Chaitanya";
        switch (bestie) {
            case "Chaitanya":
                switch ("LIFE") {
                    case "LIFE":
                        System.out.println("Dear "+bestie + " ,You are the greatest blessing in my LIFE.");
                    case "FRIENDSHIP":
                        System.out.println("As the captain of my ship of hope, you’ve guided our FRIENDSHIP through every challenge and joy.(\uD83D\uDD4A\uFE0F)");
                    case "HAPPINESS":
                        System.out.println(" You’ve shown me that true HAPPINESS often lies in the smallest of things.(☺\uFE0F)");
                    case "WISH":
                        System.out.println("My deepest WISH is for all your dreams to come true as if by magic.(\uD83E\uDE84)");
                    case "LOVE":
                        System.out.println(" I will cherish and LOVE you until my very last day on Earth.(\uD83C\uDF0E)");
                    default:
                        System.out.println(" --------HAPPY FRIENDSHIP DAY CHAITANYA(❤\uFE0F)---------  ");
                }
        }
    }
}
